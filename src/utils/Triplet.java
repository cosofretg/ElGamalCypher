package utils;

public class Triplet {
    private int x;
    private int a;
    private int b;

    public Triplet(int x, int a, int b) {
        this.x = x;
        this.a = a;
        this.b = b;
    }

    public Triplet() {
    }

    public int getX() {
        return x;
    }

    public void setX(int x) {
        this.x = x;
    }

    public int getA() {
        return a;
    }

    public void setA(int a) {
        this.a = a;
    }

    public int getB() {
        return b;
    }

    public void setB(int b) {
        this.b = b;
    }
}
